def build_entry(idx: int, raw: tuple) -> dict:
    question, answer, animal, category, subcategory, urgency, hospital_needed = raw

    import re

    def extract_key_sentences(text: str, n: int = 3) -> list[str]:
        sentences = [s.strip() for s in re.split(r'[.!?\n]', text) if len(s.strip()) > 15]
        priority = ["병원", "치료", "증상", "원인", "예방", "주의", "검사", "응급"]
        scored = [(sum(1 for k in priority if k in s), s) for s in sentences]
        scored.sort(key=lambda x: -x[0])
        return [s for _, s in scored[:n]]

    symptoms = []
    answer_lower = answer
    symptom_keywords = {
        "구토": "구토", "설사": "설사", "식욕부진": "식욕 저하",
        "기침": "기침", "재채기": "재채기", "가려움": "소양증",
        "탈모": "탈모", "발적": "피부 발적", "분비물": "분비물",
        "황달": "황달", "다음": "과도한 음수", "다뇨": "다뇨",
        "파행": "파행(절음)", "발작": "발작", "의식": "의식 변화",
    }
    for kw, label in symptom_keywords.items():
        if kw in question or kw in answer_lower:
            if label not in symptoms:
                symptoms.append(label)

    return {
        "id": f"seed_{idx:04d}",
        "source": "fallback_handcrafted",
        "animal": animal,
        "category": category,
        "subcategory": subcategory,
        "urgency": urgency,
        "symptoms": symptoms[:5],
        "diagnosis_hints": [],
        "treatment_direction": "",
        "hospital_needed": hospital_needed,
        "raw_qa": {
            "question": question,
            "answer": answer.strip(),
        },
        "key_sentences": extract_key_sentences(answer),
        "quality_score": 0.9,
        "created_at": datetime.now().isoformat(),
    }


